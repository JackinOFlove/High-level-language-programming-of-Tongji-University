/*2253744 林觉凯 软工*/
#include <iostream>
#include<iomanip>
#define MAX 13
using namespace std;
/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：只给出了上下标尺，其它均需要自己补充
***************************************************************************/
int main()
{
	int rows;
	cout << "请输入要打印的行数[3..13]" << endl;
	cin >> rows;
	/* 上标尺 */
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	int array[MAX][MAX] = { 0 };
	for (int i = 0; i < rows; i++)
	{
		array[i][0] = 1;
		array[i][i] = 1;
	}
	for (int i = 2; i < rows; i++)
	{
		for (int j = 1; j < i; j++)
			array[i][j] = array[i - 1][j - 1] + array[i - 1][j];
	}
	for (int i = 0; i < rows; i++)
	{
		int tempi = rows-i;
		for (; tempi > 1; tempi--)
			cout << "   ";
		for (int j = 0; j <= i; j++)
		{
			if (array[i][j] != 0)
				cout <<  setw(6) << array[i][j];
		}
		cout << endl;
	}
	/* 下标尺 */
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << endl;

	return 0;
}

